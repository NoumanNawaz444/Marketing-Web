const rangeSlider = document.getElementById('range-slider');
const priceDiv = document.getElementById('price-div');

rangeSlider.addEventListener('input', function() {
  const sliderValue = this.value;
  const priceValue = `$${sliderValue}`;
  priceDiv.innerHTML = priceValue;
  
  if (sliderValue >= 50) {
    priceDiv.classList.remove('hide');
    priceDiv.classList.add('show');
  } else {
    priceDiv.classList.remove('show');
    priceDiv.classList.add('hide');
  }
});


